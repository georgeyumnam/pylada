def binding_energy(crystal, structure):
  """ Computes binding energy, including BS superposition correction.

      The binding energy is defined as:
  
      :param crystal:
        Functional with which to minimize the structure. 
  """
  from pylada.jobfolder import JobFolder
  from pylada.dftcrystal import Molecule, RemoveAtoms
  from pylada.error import ValueError
  from pylada.crystal import neighbors
  system = structure.copy()
  # look for index op where molecule is added.
  for addmol, value in enumerate(system): 
    if value.keyword.lower() == 'atominse': break
  if addmol == len(system) and system[-1].keyword.lower() != 'atominse':
    raise ValueError('Could not determine adsorbed molecule')
  if addmol == 0: 
    raise ValueError('Could not determine adsorbed molecule')

  # look for isolated surface index
  for iclsurf, value in enumerate(system[:addmol][::-1]):
    if value.keyword.lower() in ['ATOMDISP']: break
  iclsurf = addmol - iclsurf

  # now create different systems.

  # the isolated surface
  iso_surface = structure.copy()
  iso_surface[:] = iso_surface[:iclsurf]

  # the molecule
  iso_mol = Molecule()
  iso_mol.atoms = structure[addmol].atoms

  # figures out atoms added to surface
  maxlabel = max(a.label for a in iso_surface.eval())
  mols = [a for a in structure.eval() if a.label > maxlabel]
  # surface, distorted geometry, eg after relaxation from absorption
  dist_surface = structure.copy()
  labels = [a.label for a in mols if a.asymmetric]
  dist_surface.append(RemoveAtoms(*labels, keepsym=True))

  # surface, distorted geometry, eg after relaxation from absorption
  dist_surface = structure.copy()
  labels = [a.label for a in mols if a.asymmetric]
  dist_surface.append(RemoveAtoms(*labels, keepsym=True))

  # creates distorted molecule
  N = len(iso_mol.atoms)
  pos = iso_mol.atoms[0].pos
  atoms = [u[0] for u in neighbors(structure.eval(), N, pos)]
  if len(atoms) != N: raise ValueError('Could not determine distorted molecule')
  dist_mol = Molecule()
  dist_mol.atoms = atoms

  # now creates associated jobs.
  rootjob = JobFolder()
  rootjob.functional = crystal
  rootjob.params['structure'] = structure

  job = rootjob / 'molecule' / 'isolated' 
  job.functional = crystal
  job.params['structure']  = iso_mol

  job = rootjob / 'surface' / 'isolated' 
  job.functional = crystal
  job.params['structure']  = iso_surface

  job = rootjob / 'molecule' / 'distorted' 
  job.functional = crystal
  job.params['structure']  = dist_mol

  job = rootjob / 'surface' / 'distorted' 
  job.functional = crystal
  job.params['structure']  = dist_surface

  job = rootjob / 'molecule' / 'ghosted' 
  job.functional = crystal
  job.params['structure']  = structure
  job.functional.basis.ghosts = [ u.label for u in structure.eval()            \
                                  if u.asymmetric and u.label <= maxlabel ],   \
                                False

  job = rootjob / 'surface' / 'ghosted' 
  job.functional = crystal
  job.params['structure']  = structure
  job.functional.basis.ghosts = [ u.label for u in structure.eval()            \
                                  if u.asymmetric and u.label > maxlabel ],    \
                                False
  return rootjob
